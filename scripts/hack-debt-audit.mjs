#!/usr/bin/env node

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(SCRIPT_DIR, '..');
const JS_DIR = path.join(ROOT, 'js');

const PATTERNS = [
    { kind: 'seed-specific', level: 'suspicious', re: /\bseed\d{4}\b/ },
    { kind: 'override-screen', level: 'suspicious', re: /_override_screen/ },
    { kind: 'fastforward/replay', level: 'suspicious', re: /\bfastforward\b|replay/i },
    { kind: 'step-specific', level: 'suspicious', re: /\bstep(?:Num|Index|Idx|No)\b|screen\s*===?\s*\d+/ },
    { kind: 'debug-console', level: 'hard', re: /console\.log|console\.debug|console\.warn/ },
    { kind: 'fs-write', level: 'hard', re: /writeFileSync|appendFileSync|createWriteStream/ },
    { kind: 'frozen-import', level: 'hard', re: /from\s+['"][^'"]*frozen\// },
];

function parseArgs(argv) {
    const out = {
        strict: argv.includes('--strict'),
        markdown: argv.includes('--markdown'),
        write: argv.includes('--write'),
        all: argv.includes('--all'),
        limit: 20,
    };
    for (let i = 0; i < argv.length; i++) {
        if (argv[i] === '--limit') out.limit = Number(argv[++i] || out.limit);
        else if (argv[i].startsWith('--limit=')) out.limit = Number(argv[i].slice(8));
    }
    return out;
}

function listJsFiles(dir) {
    const out = [];
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
        const p = path.join(dir, entry.name);
        if (entry.isDirectory()) out.push(...listJsFiles(p));
        else if (entry.isFile() && entry.name.endsWith('.js')) out.push(p);
    }
    return out;
}

export function auditHackDebt() {
    const findings = [];
    for (const file of listJsFiles(JS_DIR)) {
        const rel = path.relative(ROOT, file);
        const lines = readFileSync(file, 'utf8').split(/\r?\n/);
        for (let i = 0; i < lines.length; i++) {
            const line = lines[i];
            for (const pattern of PATTERNS) {
                if (!pattern.re.test(line)) continue;
                findings.push({
                    file: rel,
                    line: i + 1,
                    kind: pattern.kind,
                    level: pattern.level,
                    text: line.trim().slice(0, 160),
                });
            }
        }
    }
    return findings;
}

export function renderHackDebt(findings, options = {}) {
    const markdown = typeof options === 'boolean' ? options : Boolean(options.markdown);
    const limit = typeof options === 'object' && options.all ? findings.length : Number(options.limit ?? 20);
    const hard = findings.filter((f) => f.level === 'hard').length;
    const suspicious = findings.filter((f) => f.level === 'suspicious').length;
    const lines = [];
    lines.push(markdown ? '# Hack Debt Audit' : 'hack-debt audit');
    lines.push(markdown ? '' : `hard=${hard} suspicious=${suspicious}`);
    if (markdown) lines.push(`Generated by \`node scripts/hack-debt-audit.mjs --markdown\`.\n`);
    if (markdown) {
        lines.push('| Level | Kind | Location | Text |');
        lines.push('|---|---|---|---|');
        for (const f of findings) {
            lines.push(`| ${f.level} | ${f.kind} | ${f.file}:${f.line} | ${f.text.replace(/\|/g, '\\|')} |`);
        }
    } else {
        const shown = findings.slice(0, Number.isFinite(limit) ? limit : 20);
        for (const f of shown) {
            lines.push(`${f.level.padEnd(10)} ${f.kind.padEnd(18)} ${f.file}:${f.line} ${f.text}`);
        }
        if (shown.length < findings.length) {
            lines.push(`showing ${shown.length}/${findings.length}; rerun with --all or --limit N for more`);
        }
    }
    if (!findings.length) lines.push(markdown ? 'No findings.' : 'no findings');
    return lines.join('\n');
}

function main() {
    const options = parseArgs(process.argv.slice(2));
    const findings = auditHackDebt();
    const output = renderHackDebt(findings, options);
    if (options.write) writeFileSync(path.join(ROOT, 'scratch', 'hack-debt.md'), `${output}\n`);
    console.log(output);

    const hasHard = findings.some((f) => f.level === 'hard');
    if (hasHard || (options.strict && findings.length)) process.exit(1);
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
    main();
}
